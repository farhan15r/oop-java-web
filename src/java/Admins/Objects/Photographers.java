package Admins.Objects;

public class Photographers {
  private int id;
  private String fullName;

  public void setId(int id) {
    this.id = id;
  }

  public void setFullName(String name) {
    this.fullName = name;
  }

  public int getId() {
    return id;
  }

  public String getFullName() {
    return fullName;
  }
}
